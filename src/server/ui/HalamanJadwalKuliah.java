    private void TabelJadwalKuliahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelJadwalKuliahMouseClicked
        // TODO add your handling code here:
        int row = TabelJadwalKuliah.getSelectedRow();
        id = Integer.parseInt(TabelJadwalKuliah.getModel().getValueAt(row, 0).toString());
        String nama = TabelJadwalKuliah.getModel().getValueAt(row, 1).toString();
        String jam = TabelJadwalKuliah.getModel().getValueAt(row, 2).toString();
        String hari = TabelJadwalKuliah.getModel().getValueAt(row, 3).toString();
        String kelas = TabelJadwalKuliah.getModel().getValueAt(row, 4).toString();
        String semester = TabelJadwalKuliah.getModel().getValueAt(row, 5).toString();

        TFNama.setText(nama);
        TFJam.setText(jam);
        TFHari.setText(hari);
        TFKelas.setText(kelas);
        TFSemester.setText(semester);
    }//GEN-LAST:event_TabelJadwalKuliahMouseClicked

