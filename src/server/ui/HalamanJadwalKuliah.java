    public void loadDataJadwalKuliah() {
        try {
            DefaultTableModel DTM = new DefaultTableModel();
            DTM.addColumn("ID");
            DTM.addColumn("Nama");
            DTM.addColumn("Jam");
            DTM.addColumn("Hari");
            DTM.addColumn("Kelas");
            DTM.addColumn("Semester");
            DTM.addColumn("Waktu Tambah");
            DTM.addColumn("Waktu Ubah");

            List<ModelJadwalKuliah> data = rmi.getDataJadwalKuliah();
            System.out.println(data.get(0).getNama());
            for (ModelJadwalKuliah modelJadwalKuliah : data) {
                DTM.addRow(new Object[]{modelJadwalKuliah.getId_praktikum(), modelJadwalKuliah.getNama(), modelJadwalKuliah.getJam(), modelJadwalKuliah.getHari(), modelJadwalKuliah.getKelas(), modelJadwalKuliah.getSemester(), modelJadwalKuliah.getCreated_at(), modelJadwalKuliah.getUpdated_at()});
                TabelJadwalKuliah.setModel(DTM);
            }

        } catch (RemoteException ex) {
            Logger.getLogger(HalamanJadwalKuliah.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

