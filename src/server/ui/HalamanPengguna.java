    public void loadDataJadwalKuliah() {
        try {
            DefaultTableModel DTM = new DefaultTableModel();
            DTM.addColumn("ID");
            DTM.addColumn("Username");
            DTM.addColumn("Password");
            DTM.addColumn("Waktu Tambah");
            DTM.addColumn("Waktu Ubah");

            List<ModelUser> data = rmi.getDataUser();
            for (ModelUser modelUser : data) {
                DTM.addRow(new Object[]{modelUser.getId(), modelUser.getUsername(), modelUser.getPassword(), modelUser.getCreated_at(), modelUser.getUpdated_at()});
                TabelDataPengguna.setModel(DTM);
            }

        } catch (RemoteException ex) {
            Logger.getLogger(HalamanJadwalKuliah.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

