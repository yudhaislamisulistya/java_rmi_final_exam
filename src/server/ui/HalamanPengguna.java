    private void TabelDataPenggunaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelDataPenggunaMouseClicked
        // TODO add your handling code here:

        int row = TabelDataPengguna.getSelectedRow();
        id = Integer.parseInt(TabelDataPengguna.getModel().getValueAt(row, 0).toString());
        String username = TabelDataPengguna.getModel().getValueAt(row, 1).toString();
        String password = TabelDataPengguna.getModel().getValueAt(row, 2).toString();

        TFUsername.setText(username);
        TFPassword.setText(password);
    }//GEN-LAST:event_TabelDataPenggunaMouseClicked

